public class PublisherEvent extends Event {
	
	private String publisherName;
	
	public PublisherEvent(int id, String name, String publisherName) {
		super(id, name);
		this.publisherName = publisherName;
	}
	
	public String getPublisherName() {
		return publisherName;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		PublisherEvent other = (PublisherEvent) obj;
		
		if (publisherName != other.publisherName) {
			return false;
		}
		if (id != other.id) {
			return false;
		}
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}
	
}